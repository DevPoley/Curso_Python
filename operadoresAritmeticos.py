#SUMA  RESTO  DIVISIÓN  MULTIPLICACIÓN  DIVISIÓN  ENTERA  MÓDULO  EXPONENTE
# ORDEN DE PREFERENCIA DE LOS OPERADORES 
'''PARÉNTESIS: () , EXPONENTE: ** , NEGACIÓN: - , MULTIPLICACIÓN: * , DIVISIÓN: / , DIVISIÓN ENTERA: // , MÓDULO: % , SUMA: + , RESTA: - '''
num1 = 5
num2 = 3
print("******************************************")
resultado = num1 + num2
print("La suma es : ",resultado)
resultado = num1 - num2
print("La resta es : ",resultado)
resultado = num1 * num2
print("La multiplicación es : ",resultado)
resultado = num1 / num2
print("La división es : ",resultado)
resultado = num1 // num2
print("La división entera es : ",resultado)
resultado = num1 % num2
print("El módulo es (resto de la operación) : ",resultado)
resultado = num1 ** num2
print("El exponente es : ",resultado)
print("******************************************")
print("***************FÓRMULA****************")
resultado = 3**3 * (13/5 * (2*4))
print("El resultado de la Fórmula es",resultado)

